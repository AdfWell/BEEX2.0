class Carta{
    reto
    color

    constructor(reto, color) {
        this.reto = reto;
        this.color = color;
    }

    get reto() {
        return this.reto;
    }

    set reto(value) {
        this.reto = value;
    }

    get color() {
        return this.color;
    }

    set color(value) {
        this.color = value;
    }
}